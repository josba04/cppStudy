#include <iostream>
using namespace std;

int main() {
	cout << "char 자료형의 크기는 " << sizeof(char) << endl;
	cout << "short 자료형의 크기는 " << sizeof(short) << endl;
	cout << "int 자료형의 크기는 " << sizeof(int) << endl;
	cout << "float 자료형의 크기는 " << sizeof(float) << endl;
	cout << "double 자료형의 크기는 " << sizeof(double) << endl;
	
	cout << "unsigned char 자료형의 크기는 " << sizeof(unsigned char) << endl;
	cout << "unsigned short 자료형의 크기는 " << sizeof(unsigned short) << endl;
	cout << "unsigned int 자료형의 크기는 " << sizeof(unsigned int) << endl;
	
	return 0;

}